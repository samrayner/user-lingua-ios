// Opacity.swift

import Foundation

extension Double {
    /// Opacity constants.
    package enum Opacity {
        /// 0%
        package static let transparent = 0.0

        /// 25%
        package static let light = 0.25

        /// 50%
        package static let medium = 0.5

        /// 75%
        package static let heavy = 0.75

        /// 100%
        package static let opaque = 1.0
    }
}
