// Opacity.swift

import Foundation

extension Double {
    /// Opacity constants.
    public enum Opacity {
        /// 0%
        public static let transparent = 0.0

        /// 25%
        public static let light = 0.25

        /// 50%
        public static let medium = 0.5

        /// 75%
        public static let heavy = 0.75

        /// 100%
        public static let opaque = 1.0
    }
}
